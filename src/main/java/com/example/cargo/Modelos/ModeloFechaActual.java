package com.example.cargo.Modelos;

public class ModeloFechaActual {

    private String fechaDia;
    private String fechaMes;
    private String fechaMinuto;
    private String fechaHora;
    private String fechaDiaSemana;

    public String getFechaDia() {
        return fechaDia;
    }

    public void setFechaDia(String fechaDia) {
        this.fechaDia = fechaDia;
    }

    public String getFechaMes() {
        return fechaMes;
    }

    public void setFechaMes(String fechaMes) {
        this.fechaMes = fechaMes;
    }

    public String getFechaMinuto() {
        return fechaMinuto;
    }

    public void setFechaMinuto(String fechaMinuto) {
        this.fechaMinuto = fechaMinuto;
    }

    public String getFechaHora() {
        return fechaHora;
    }

    public void setFechaHora(String fechaHora) {
        this.fechaHora = fechaHora;
    }

    public String getFechaDiaSemana() {
        return fechaDiaSemana;
    }

    public void setFechaDiaSemana(String fechaDiaSemana) {
        this.fechaDiaSemana = fechaDiaSemana;
    }

}
